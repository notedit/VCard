import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import os from 'node:os';
import path from 'node:path';

export const REQUIRED_DEV_VAR_KEYS = ['DATABASE_URL'];
export const OPTIONAL_DEV_VAR_KEYS = ['AIHUBMIX_API_KEY', 'TAVILY_API_KEY'];
const ALL_DEV_VAR_KEYS = [...REQUIRED_DEV_VAR_KEYS, ...OPTIONAL_DEV_VAR_KEYS];

export function syncDevVars(root = process.cwd()) {
  const source = path.join(os.homedir(), '.secrets', 'common.env');
  const targetDir = path.join(root, 'apps', 'api');
  const target = path.join(targetDir, '.dev.vars');

  if (!existsSync(source)) {
    throw new Error(`Missing ${source}`);
  }

  const sourceText = readFileSync(source, 'utf8');
  const sourceLines = sourceText.split(/\r?\n/);
  const selected = new Map();

  for (const line of sourceLines) {
    const match = line.match(/^\s*(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=/);
    if (!match) continue;
    const key = match[1];
    if (!ALL_DEV_VAR_KEYS.includes(key)) continue;
    selected.set(key, line.replace(/^\s*export\s+/, ''));
  }

  const missing = REQUIRED_DEV_VAR_KEYS.filter((key) => !selected.has(key));
  if (missing.length > 0) {
    throw new Error(`Missing required key(s) in ${source}: ${missing.join(', ')}`);
  }

  const written = ALL_DEV_VAR_KEYS.filter((key) => selected.has(key));

  mkdirSync(targetDir, { recursive: true });
  const output = [
    '# Generated by npm run dev:vars. Do not add unrelated secrets here.',
    ...written.map((key) => selected.get(key)),
    '',
  ].join('\n');
  writeFileSync(target, output);
  return { source, target, keys: written };
}
